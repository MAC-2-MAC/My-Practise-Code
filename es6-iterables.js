let days = [
    'monday',
    'tuesday',
    'wednesday',
    'thursday'
];

for( let item of days){ // this is used to iterate over all elements in the array as the array follows the indexing method to print, same  we can use with strings also 
    console.log(item);
}

let char = "Good Morning";

